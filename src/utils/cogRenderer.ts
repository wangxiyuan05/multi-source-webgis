/**
 * COGTiff 渲染器：用 geotiff 读取波段数据 → 渲染到 Canvas →
 * 通过 Cesium Rectangle 实体贴地显示。
 * 完全规避 TIFF 无地理参考导致的定位问题。
 */
import * as Cesium from 'cesium'
import { fromUrl } from 'geotiff'

let geoTiff: any = null

export async function initCogReader(url: string) {
  geoTiff = await fromUrl(url)
  console.log('[COG] 初始化成功, images:', geoTiff.getImageCount())
  return geoTiff
}

/** 读取指定波段（使用第 2 级金字塔） */
export async function readBand(bandIndex: number): Promise<{
  data: Float32Array
  width: number
  height: number
  min: number
  max: number
} | null> {
  if (!geoTiff) {
    console.warn('[COG] 未初始化')
    return null
  }
  try {
    const levels = geoTiff.getImageCount()
    const level = Math.min(2, levels - 1)
    const image = await geoTiff.getImage(level)
    const w = image.getWidth()
    const h = image.getHeight()
    console.log(`[COG] 读取波段 ${bandIndex}, 层级 ${level}, 尺寸 ${w}×${h}`)

    const raster = await image.readRasters({ samples: [bandIndex - 1] })
    const data = raster[0] as Float32Array

    let min = Infinity, max = -Infinity
    for (let i = 0; i < data.length; i++) {
      const v = data[i]
      if (v < min) min = v
      if (v > max) max = v
    }
    console.log(`[COG] 波段 ${bandIndex} 范围: ${min.toFixed(6)} ~ ${max.toFixed(6)}`)
    return { data, width: w, height: h, min, max }
  } catch (err) {
    console.error('[COG] readBand 失败:', err)
    return null
  }
}

/** 渲染到 Canvas（使用颜色查找表） */
export function renderToCanvas(
  data: Float32Array,
  width: number,
  height: number,
  min: number,
  max: number,
  colorScale: string,
): HTMLCanvasElement | null {
  try {
    const canvas = document.createElement('canvas')
    canvas.width = width
    canvas.height = height
    const ctx = canvas.getContext('2d')
    if (!ctx) { console.error('[COG] Canvas 2D 不可用'); return null }

    const imageData = ctx.createImageData(width, height)
    const pixels = imageData.data
    const palette = getPalette(colorScale)

    if (max === min) max = min + 1

    for (let i = 0; i < data.length; i++) {
      const t = (data[i] - min) / (max - min)
      const idx = Math.max(0, Math.min(palette.length / 4 - 1, Math.floor(t * (palette.length / 4 - 1)))) * 4
      pixels[i * 4] = palette[idx]
      pixels[i * 4 + 1] = palette[idx + 1]
      pixels[i * 4 + 2] = palette[idx + 2]
      pixels[i * 4 + 3] = 255
    }

    ctx.putImageData(imageData, 0, 0)
    return canvas
  } catch (err) {
    console.error('[COG] renderToCanvas 失败:', err)
    return null
  }
}

/** 读取指定位置所有波段的 DN 值 */
export async function readPixelAllBands(
  lon: number,
  lat: number,
  rect: { west: number; south: number; east: number; north: number },
): Promise<{ bands: number[]; values: number[] } | null> {
  if (!geoTiff) return null
  try {
    const image = await geoTiff.getImage(0)
    const w = image.getWidth()
    const h = image.getHeight()

    const px = ((lon - rect.west) / (rect.east - rect.west)) * w
    const py = ((rect.north - lat) / (rect.north - rect.south)) * h
    const ix = Math.round(Math.max(0, Math.min(w - 1, px)))
    const iy = Math.round(Math.max(0, Math.min(h - 1, py)))

    const samples = image.getSamplesPerPixel()
    const bands: number[] = []
    const values: number[] = []
    for (let b = 0; b < samples; b++) {
      const raster = await image.readRasters({ samples: [b], window: [ix, iy, ix + 1, iy + 1] })
      bands.push(b + 1)
      values.push(raster[0][0])
    }
    return { bands, values }
  } catch (err) {
    console.error('[COG] readPixelAllBands 失败:', err)
    return null
  }
}

